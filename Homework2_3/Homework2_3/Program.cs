using System;

namespace Homework2_3 {
    class Program {
        static void Main(string[] args) {
            int x = 5;
            int y = 12;
            int z = 2;

            /*
             * Decription
             * x++ - --y == 5 - 11 == -6
             * x == 6, y == 11, z == 2
             * 
             * x++ - --y + --x == -6 + 5 == -1
             * x == 5, y == 11, z == 2
             * 
             * (x++ - --y + --x << z++) == -1 << 2 == -4
             * =========================================================================================
             * (Int32) -1                                   == 1111 1111 1111 1111 1111 1111 1111 1111
             * 1111 1111 1111 1111 1111 1111 1111 1111 << 2 == 1111 1111 1111 1111 1111 1111 1111 1100
             * 1111 1111 1111 1111 1111 1111 1111 1100      == (Int32) -4
             * =========================================================================================
             * x == 5, y == 11, z == 3
             * 
             * x + y + z == 5 + 11 + 3 == 19
             * x == 5, y == 11, z == 3
             * 
             * (x++ - --y + --x << z++) * (x + y + z) == -4 * 19 == -76
             */

            int result = (x++ - --y + --x << z++) * (x + y + z);

            Console.WriteLine(result);
        }
    }
}
